export const colorsRGB = defaultColors.map(hexToRGb);

function cutHex(h: string) {
  return h.charAt(0) == '#' ? h.substring(1, 7) : h;
}

function hexToRGb(hex: string) {
  return {
    red: parseInt(cutHex(hex).substring(0, 2), 16),
    green: parseInt(cutHex(hex).substring(2, 4), 16),
    blue: parseInt(cutHex(hex).substring(4, 6), 16),
  };
}
